package data.animal;

public class AnimalResponse {
	AnimalHeader header;
	AnimalBody body;
	
	public AnimalHeader getHeader() {
		return header;
	}
	public void setHeader(AnimalHeader header) {
		this.header = header;
	}
	public AnimalBody getBody() {
		return body;
	}
	public void setBody(AnimalBody body) {
		this.body = body;
	}
	
	
}
